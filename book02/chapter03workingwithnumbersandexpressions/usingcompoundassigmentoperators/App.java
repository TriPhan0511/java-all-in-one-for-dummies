package book02.chapter03workingwithnumbersandexpressions.usingcompoundassigmentoperators;

public class App {
    public static void main(String[] args) {
        int a = 10;
        a += 3; // a = 13
        a -= 4; // a = 9
        a *= 2; // a = 18
        a /= 3; // a = 6
        a %= 4; // a = 2
    }
}
